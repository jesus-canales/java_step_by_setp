package com.entrecodigosycafe.operators;

public class OperadoresRelacionales {

    public static void ejecutar () {
        int valorA = 5;
        int valorB = 8;

        // Igual a
        System.out.println(" ¿a == b? " + (valorA == valorB));

        // Diferente de
        System.out.println(" ¿a != b? " + (valorA != valorB));

        // Mayor que
        System.out.println(" ¿a > b? " + (valorA > valorB));

        // Menor que
        System.out.println(" ¿a < b? " + (valorA < valorB));

        // Mayor o igual que
        System.out.println(" ¿a >= b? " + (valorA >= valorB));

        // Menor o igual que
        System.out.println(" ¿a <= b? " + (valorA <= valorB));

    }
}
